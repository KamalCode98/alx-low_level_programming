#include "main.h"

/**
 * mul - Entry point
 *
 * *@a : Variable that represent the first number
 *
 * *@b : Variable that represent the second number
 *
 * Description: Function that multiplies two integers
 *
 * Prototype: int mul(int a, int b);
 *
 * Return: Always 0 (Success)
 */
int mul(int a, int b)
{
	int result;

	result = a * b;

	return (result);
}
